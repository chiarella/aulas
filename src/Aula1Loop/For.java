package Aula1Loop;

public class For {

	public static void main(String[] args) {
		
		
		/* O QUE É: 
		 * For = loop = laço (Estrutura de repetição)
		 * 
		 * COMO FAZER:
		 * Para criarmos o FOR é necessário:  		 * 
		 * Expressão 1  : int i = 0
		 * Condição     : i < 11
		 * Expressão 2  : i ++ "incremento"
		 * 
		 */
		
		//OBS: Podemos declarar dentrou ou fora do escopo.
		int i;
		
		for (i = 1 ; i < 11; i++) {
			
			System.out.println(i);
			
		}

	}

}
